export default function positions(state = initialState, action) {
  switch (action.type) {
    case POP_BALLOON:
      return state.map((balloon) =>
        balloon.id === action.id ? { ...balloon, show: !balloon.show } : balloon
      );
    case ADD_BALLOON:
      return [...state, action.balloon];
    default:
      return state;
  }
}
